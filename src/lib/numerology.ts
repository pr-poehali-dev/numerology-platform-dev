export function calculateSoulNumber(fullName: string): number {
  const vowels = 'аеёиоуыэюяaeiouy';
  const map: Record<string, number> = {
    a:1,e:5,i:9,o:6,u:3,y:7,
    а:1,е:6,ё:7,и:1,о:7,у:3,ы:2,э:5,ю:3,я:6
  };
  const sum = fullName.toLowerCase().split('').filter(c => vowels.includes(c))
    .reduce((s, c) => s + (map[c] || 0), 0);
  return reduceToSingleDigit(sum || 1);
}

export function calculatePersonality(fullName: string): number {
  const vowels = 'аеёиоуыэюяaeiouy';
  const map: Record<string, number> = {
    б:2,в:3,г:4,д:5,ж:8,з:9,й:2,к:3,л:4,м:5,н:6,п:8,р:9,с:1,т:2,ф:4,х:5,ц:6,ч:7,ш:8,щ:9,ъ:1,ь:3,
    b:2,c:3,d:4,f:6,g:7,h:8,j:1,k:2,l:3,m:4,n:5,p:7,q:8,r:9,s:1,t:2,v:4,w:5,x:6,z:8
  };
  const sum = fullName.toLowerCase().split('').filter(c => !vowels.includes(c) && /[a-zа-яё]/.test(c))
    .reduce((s, c) => s + (map[c] || 1), 0);
  return reduceToSingleDigit(sum || 1);
}

