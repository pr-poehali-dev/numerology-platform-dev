export function calculatePersonalYear(birthDate: string, year?: number): number {
  const date = new Date(birthDate);
  const targetYear = year ?? new Date().getFullYear();
  const day = date.getDate();
  const month = date.getMonth() + 1;
  const sum = String(day).split('').reduce((s, d) => s + parseInt(d), 0)
    + String(month).split('').reduce((s, d) => s + parseInt(d), 0)
    + String(targetYear).split('').reduce((s, d) => s + parseInt(d), 0);
  return reduceToSingleDigit(sum);
}

